/**
 * bundle-migrations.mjs
 *
 * Reads the Drizzle migration journal + SQL files and generates a TypeScript
 * module that browser code can import. Each migration becomes a named export
 * containing the SQL split on "--> statement-breakpoint", plus metadata
 * (hash, timestamp) for the __drizzle_migrations tracking table.
 *
 * Run:  node scripts/bundle-migrations.mjs
 * Hook: add to postinstall alongside copy-pglite-assets.mjs
 */

import { createHash } from "node:crypto";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = join(__dirname, "..");
const DRIZZLE_DIR = join(projectRoot, "drizzle");
const JOURNAL_PATH = join(DRIZZLE_DIR, "meta", "_journal.json");
const OUT_DIR = join(projectRoot, "src", "db", "migrations");
const OUT_FILE = join(OUT_DIR, "generated-migrations.ts");

if (!existsSync(JOURNAL_PATH)) {
  console.warn(`[bundle-migrations] ${JOURNAL_PATH} not found; skipping.`);
  process.exit(0);
}

const journal = JSON.parse(readFileSync(JOURNAL_PATH, "utf8"));

function stripBom(s) {
  return s.replace(/^\uFEFF/, "");
}

const migrations = journal.entries.map((entry) => {
  const sqlPath = join(DRIZZLE_DIR, `${entry.tag}.sql`);
  if (!existsSync(sqlPath)) {
    console.error(`[bundle-migrations] Missing: ${sqlPath}`);
    process.exit(1);
  }

  const rawSql = stripBom(readFileSync(sqlPath, "utf8"));
  const statements = rawSql
    .split("--> statement-breakpoint")
    .map((s) => stripBom(s).trim())
    .filter((s) => s.length > 0);

  const hash = createHash("sha256").update(rawSql).digest("hex");

  return {
    idx: entry.idx,
    tag: entry.tag,
    when: entry.when,
    hash,
    statements,
  };
});

// Generate TypeScript
const lines = [
  "/**",
  " * AUTO-GENERATED by scripts/bundle-migrations.mjs — DO NOT EDIT",
  " *",
  " * Contains all Drizzle migration SQL for the browser OPFS database.",
  " * Each migration mirrors the corresponding drizzle/*.sql file.",
  " */",
  "",
  "export type BundledMigration = {",
  "  idx: number;",
  "  tag: string;",
  "  when: number;",
  "  hash: string;",
  "  statements: string[];",
  "};",
  "",
];

for (const m of migrations) {
  lines.push(`export const migration_${String(m.idx).padStart(4, "0")}: BundledMigration = {`);
  lines.push(`  idx: ${m.idx},`);
  lines.push(`  tag: ${JSON.stringify(m.tag)},`);
  lines.push(`  when: ${m.when},`);
  lines.push(`  hash: ${JSON.stringify(m.hash)},`);
  lines.push(`  statements: [`);
  for (const stmt of m.statements) {
    lines.push(`    ${JSON.stringify(stmt)},`);
  }
  lines.push(`  ],`);
  lines.push(`};`);
  lines.push(``);
}

// Export the ordered array
lines.push(`export const allMigrations: BundledMigration[] = [`);
for (const m of migrations) {
  lines.push(`  migration_${String(m.idx).padStart(4, "0")},`);
}
lines.push(`];`);
lines.push(``);

mkdirSync(OUT_DIR, { recursive: true });
writeFileSync(OUT_FILE, lines.join("\n"), "utf8");
console.log(
  `[bundle-migrations] Generated ${OUT_FILE} with ${migrations.length} migrations.`
);
